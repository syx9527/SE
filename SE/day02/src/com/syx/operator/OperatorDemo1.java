package com.syx.operator;

public class OperatorDemo1 {
    public static void main(String[] args) {
        // 目标：掌握基本算数 运算 + - * / %
        int a = 10;
        int b = 3;

        System.out.println(a + b);
        System.out.println((a * b));
        System.out.println(a - b);
        System.out.println(a / b);
        System.out.println(a % b);


        System.out.println("-------------------");
        double c = 10.3;
        double d = 3.3;

        System.out.println(c + d);
        System.out.println((c * d));
        System.out.println(c - d);
        System.out.println(c / d);
        System.out.println(c % d);

    }
}
