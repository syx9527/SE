public class LoopTest2 {
    public static void main(String[] args) {
        int n = 90000999;
        int sqt = sqrtFor(n);
        System.out.println(sqt);
        System.out.println("---------------");
    }

    public static int sqrtFor(int n) {
        for (int i = 0; i < n; i++) {
            if (i * i <= n && (i + 1) * (i + 1) > n) {
                return i;
            }
        }
        return -1;
    }


}
